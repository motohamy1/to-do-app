export type Language = keyof typeof translations;

export const useTranslation = (lang: string = 'en') => {
  const currentLang = (lang === 'ar' ? 'ar' : 'en') as Language;
  const t = translations[currentLang];
  
  return {
    t,
    isArabic: currentLang === 'ar',
    // Helper to adjust style based on language
    alignStyle: { textAlign: currentLang === 'ar' ? 'right' : 'left' } as const,
    flexDirStyle: { flexDirection: currentLang === 'ar' ? 'row-reverse' : 'row' } as const,
  };
};
